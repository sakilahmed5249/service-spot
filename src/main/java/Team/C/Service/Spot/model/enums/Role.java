package Team.C.Service.Spot.model.enums;

/**
 * Role enumeration for user types in the Service-Spot application.
 * Defines the three primary user roles: admin, customers and service providers.
 *
 * @author Team C
 * @version 4.0
 * @since 2025-11-29
 */
public enum Role {
    /**
     * Admin role - system administrators with full access
     */
    ADMIN,

    /**
     * Customer role - users who search and book services
     */
    CUSTOMER,

    /**
     * Provider role - users who offer and manage services
     */
    PROVIDER
}


